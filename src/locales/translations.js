/**
 * Replace {key} placeholders in a string with values from params.
 */
export function interpolate(str, params = {}) {
  if (!str || typeof str !== 'string') return str
  return str.replace(/\{(\w+)\}/g, (_, key) => (params[key] != null ? String(params[key]) : `{${key}}`))
}
